#!/bin/bash
# post-compact-recovery.sh — コンパクション後の復帰フック
# SessionStart(compact) で発火する
# stdout の内容がコンテキストに追加される
# 意図的に set -e を使わない（フックは失敗してもセッションを妨げず exit 0 で抜ける設計）

echo "pwd: $(pwd)"
echo ""
cat << 'EOF'
[コンパクションが実行されました — 復帰手順]

コンテキストが圧縮され、直前の会話内容が失われています。
以下を順番に実行してください：

1. CLAUDE.md のブートシーケンスを実行する（自分が誰か、何をすべきか思い出す）
2. Progress.md・Feedback.md を確認する（直前の進捗・改善アクションを把握する）
3. TODO.md を確認する（進行中のタスクがあるか確認）
4. コンパクション前の文脈を踏まえて、自然に会話を再開する

※ このメッセージはコンパクション後に自動注入されています。
EOF

exit 0
